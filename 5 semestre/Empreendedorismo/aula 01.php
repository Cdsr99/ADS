Definição de empreendedorimo

o empreendedor será presente no nosso cotidiano com papel essencial no desenvolvimento social.
o termo empreendedorismo é a capacidade que uma pessoa tem de identificar problemas e oportunidades, desenvolver soluções e investir recursos na criação de algo positivo para a sociedade.

ideia: alguma coisa + você

Oportunidade: ideia + ação

Ação: função (integração) do dinheiro para o produto e parceiros

Viabilidade = Oportunidade + comprometimento.